<p>
  Looking for the first step in working with scuba as a career? Your adventure into the professional levels of recreational scuba diving begins with the PADI Divemaster program. Working closely with a PADI Instructor, in this program you expand your dive knowledge and hone your skills to the professional level.
</p>
<p>PADI Divemaster training develops your leadership abilities, qualifying you to supervise dive activities and assist instructors with student divers. PADI Divemaster is the prerequisite certification for both the PADI Assistant Instructor and PADI Open Water Scuba Instructor certifications.
  <br><br>
  [Prerequisites]
  <br>
   • Certified as a PADI Advanced Open Water Diver and PADI Rescue Diver<br>
   • Minimum 18 years old<br>
   • Minimum of 40 logged scuba dives<br>
   • Completed EFR Primary and Secondary Care training within 24 months<br>
   • Medically evaluated and cleared for diving by a physician within 12 months
</p>
